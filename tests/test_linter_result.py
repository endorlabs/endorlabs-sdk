    def test_linter_result_error_handling(self):
        """Test error handling for invalid UUID."""
        # Test with invalid UUID
        invalid_linter_result = linter_result.get_linter_result(
            self.client, self.parent_namespace, "invalid-uuid"
        )
        assert invalid_linter_result is None

    def test_linter_result_origin_distribution(self):
        """Test and analyze linter result origin and level distribution."""
        import conftest

        linter_results_list = linter_result.list_linter_results(
            self.client,
            self.parent_namespace,
            list_params=ListParameters(
                page_size=conftest.TEST_PAGE_SIZE,
                traverse=True,
            ),
            max_pages=conftest.TEST_MAX_PAGES_TRAVERSE,
        )

        origin_counts = {}
        level_counts = {}
        ecosystem_counts = {}

        for lr in linter_results_list:
            if lr.spec:
                origin = str(lr.spec.origin) if lr.spec.origin else "Unknown"
                origin_counts[origin] = origin_counts.get(origin, 0) + 1

                level = str(lr.spec.level) if lr.spec.level else "Unknown"
                level_counts[level] = level_counts.get(level, 0) + 1

                ecosystem = str(lr.spec.ecosystem) if lr.spec.ecosystem else "Unknown"
                ecosystem_counts[ecosystem] = ecosystem_counts.get(ecosystem, 0) + 1

        print("\n=== Linter Result Distribution ===")
        print("Origin distribution:")
        for origin, count in origin_counts.items():
            print(f"  {origin}: {count}")

        print("Level distribution:")
        for level, count in level_counts.items():
            print(f"  {level}: {count}")

        print("Ecosystem distribution:")
        for ecosystem, count in ecosystem_counts.items():
            print(f"  {ecosystem}: {count}")

        assert len(linter_results_list) > 0
