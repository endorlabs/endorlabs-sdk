"""
Test cases for ScanResult resource operations.

Tests GET and LIST operations for ScanResult resources. ScanResults are typically
auto-generated by endorctl scans and are read-only resources.
"""

import os
import sys

import pytest

# Add src to path for imports
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "src"))

from endor_cockpit.api_client import APIClient
from endor_cockpit.resources import scan_result
from endor_cockpit.types import ListParameters


@pytest.mark.integration
class TestScanResult:
    """Test cases for ScanResult resource operations."""

    @pytest.fixture(autouse=True)
    def setup(self):
        """Set up test environment."""
        self.client = APIClient()
        self.namespace = os.getenv("ENDOR_NAMESPACE", "")

        # Get test data - use parent namespace to access child resources
        # Extract parent namespace from child namespace if needed
        parts = self.namespace.split(".")
        self.parent_namespace = parts[0] if len(parts) > 1 else self.namespace

        # List scan results from parent namespace to get available data
        self.scan_results = scan_result.list_scan_results(
            self.client, self.parent_namespace
        )
        if not self.scan_results:
            pytest.skip("No scan results available for testing")

    def test_scan_result_list(self):
        """Test LIST scan results operation."""
        print("\n=== TESTING LIST SCAN RESULTS ===")

        # Test list_scan_results
        scan_results_list = scan_result.list_scan_results(
            self.client, self.parent_namespace
        )
        assert isinstance(scan_results_list, list), (
            "Should return a list of scan results"
        )
        assert len(scan_results_list) > 0, "Should have at least one scan result"

        print(f"Found {len(scan_results_list)} scan results")

        # Display first few scan results
        for _i, scan_result_item in enumerate(scan_results_list[:5]):  # Show first 5
            print(
                f"ScanResult {scan_result_item.uuid}: "
                f"{scan_result_item.meta.name if scan_result_item.meta else 'N/A'}"
            )
            if scan_result_item.spec:
                print(f"  Status: {scan_result_item.spec.status}")
                print(f"  Type: {scan_result_item.spec.type}")
                if scan_result_item.spec.start_time:
                    print(f"  Start: {scan_result_item.spec.start_time}")
                if scan_result_item.spec.end_time:
                    print(f"  End: {scan_result_item.spec.end_time}")

    def test_scan_result_get_by_uuid(self):
        """Test GET scan result by UUID operation."""
        print("\n=== TESTING GET SCAN RESULT BY UUID ===")

        scan_result_item = self.scan_results[0]
        retrieved_scan_result = scan_result.get_scan_result(
            self.client, self.parent_namespace, scan_result_item.uuid
        )

        assert retrieved_scan_result is not None, (
            "Should successfully retrieve scan result by UUID"
        )
        assert retrieved_scan_result.uuid == scan_result_item.uuid, (
            "Retrieved scan result should match original"
        )
        if retrieved_scan_result.meta and scan_result_item.meta:
            assert retrieved_scan_result.meta.name == scan_result_item.meta.name, (
                "Scan result name should match"
            )

        print(f"Successfully retrieved scan result: {retrieved_scan_result.uuid}")
        if retrieved_scan_result.meta:
            print(f"Scan result name: {retrieved_scan_result.meta.name}")
        if retrieved_scan_result.spec:
            print(f"Status: {retrieved_scan_result.spec.status}")
            print(f"Type: {retrieved_scan_result.spec.type}")

    def test_scan_result_filter_by_project(self):
        """Test filtering scan results by project UUID."""
        print("\n=== TESTING FILTER SCAN RESULTS BY PROJECT ===")

        # Get first scan result to extract project UUID
        if not self.scan_results:
            pytest.skip("No scan results available for filtering test")

        first_scan = self.scan_results[0]
        if not first_scan.meta or not first_scan.meta.parent_uuid:
            pytest.skip("Scan result has no parent_uuid (project)")

        project_uuid = first_scan.meta.parent_uuid

        # Filter scan results by project
        list_params = ListParameters(
            filter=f'meta.parent_uuid=="{project_uuid}"',
            include_child_namespaces=None,
            mask=None,
            page_size=None,
            page_token=None,
            sort_field=None,
            sort_order=None,
            count=None,
            from_date=None,
            to_date=None,
        )

        filtered_results = scan_result.list_scan_results(
            self.client, self.parent_namespace, list_params
        )

        assert isinstance(filtered_results, list), (
            "Should return a list of filtered scan results"
        )
        assert len(filtered_results) > 0, (
            "Should have at least one scan result for the project"
        )

        # Verify all results belong to the project
        for result in filtered_results:
            assert result.meta.parent_uuid == project_uuid, (
                "All filtered results should belong to the project"
            )

        print(f"Found {len(filtered_results)} scan results for project {project_uuid}")

    def test_scan_result_with_traverse(self):
        """Test listing scan results with traverse (child namespaces)."""
        print("\n=== TESTING LIST SCAN RESULTS WITH TRAVERSE ===")

        # List with traverse enabled
        list_params = ListParameters(
            filter=None,
            include_child_namespaces=True,
            mask=None,
            page_size=None,
            page_token=None,
            sort_field=None,
            sort_order=None,
            count=None,
            from_date=None,
            to_date=None,
        )

        scan_results_list = scan_result.list_scan_results(
            self.client, self.parent_namespace, list_params
        )

        assert isinstance(scan_results_list, list), (
            "Should return a list of scan results"
        )
        print(f"Found {len(scan_results_list)} scan results (with traverse)")

    def test_scan_result_structure_analysis(self):
        """Test and analyze scan result structure."""
        print("\n=== SCAN RESULT STRUCTURE ANALYSIS ===")

        scan_result_item = self.scan_results[0]
        print(f"Analyzing scan result: {scan_result_item.uuid}")

        # Analyze meta fields
        if scan_result_item.meta:
            meta_fields = [
                field
                for field in dir(scan_result_item.meta)
                if not field.startswith("_")
            ]
            print(f"ScanResult meta fields: {meta_fields}")
            if scan_result_item.meta.name:
                print(f"ScanResult name: {scan_result_item.meta.name}")
            if scan_result_item.meta.parent_uuid:
                print(f"Parent UUID (Project): {scan_result_item.meta.parent_uuid}")

        # Analyze spec fields
        if scan_result_item.spec:
            spec_fields = [
                field
                for field in dir(scan_result_item.spec)
                if not field.startswith("_")
            ]
            print(f"ScanResult spec fields: {spec_fields}")
            print(f"Status: {scan_result_item.spec.status}")
            print(f"Type: {scan_result_item.spec.type}")
            if scan_result_item.spec.environment:
                print("Environment config available")
                if scan_result_item.spec.environment.config:
                    print("  Scan config available in environment.config")

    def test_scan_result_operations_summary(self):
        """Generate summary of scan result operations."""
        print("\n=== SCAN RESULT OPERATIONS SUMMARY ===")

        print("GET Operations:")
        namespace_path = f"/v1/namespaces/{self.parent_namespace}/scan-results"
        print(f"  - List ScanResults: GET {namespace_path}")
        print(f"  - Get ScanResult: GET {namespace_path}/{{uuid}}")

        print("READ-ONLY Resource:")
        print("  - ScanResults are auto-generated by endorctl scans")
        print("  - Most fields are system-managed and immutable")
        print("  - Filtering by project UUID is supported")
        print("  - Traverse flag supports accessing child namespace resources")

        print("Success Metrics:")
        print(f"  - ScanResults Retrieved: {len(self.scan_results)}")
        print("  - GET Operations: Working")
        print("  - LIST Operations: Working")
        print("  - Filter Operations: Working")
        print("  - Traverse Operations: Working")

    def test_scan_result_base_class_inheritance(self):
        """Test that scan result inherits from base classes."""
        scan_result_item = self.scan_results[0]

        # Test BaseResource inheritance
        from endor_cockpit.models.base import BaseResource

        assert isinstance(scan_result_item, BaseResource)

        # Test BaseMeta inheritance
        from endor_cockpit.models.base import BaseMeta

        assert isinstance(scan_result_item.meta, BaseMeta)

        # Test BaseSpec inheritance
        from endor_cockpit.models.base import BaseSpec

        assert isinstance(scan_result_item.spec, BaseSpec)

    def test_scan_result_field_validation(self):
        """Test field validation and required fields."""
        scan_result_item = self.scan_results[0]

        # Verify required fields are present
        assert scan_result_item.uuid is not None
        assert scan_result_item.meta is not None
        assert scan_result_item.meta.name is not None
        assert scan_result_item.spec is not None
        assert scan_result_item.spec.status is not None
        assert scan_result_item.spec.type is not None

    def test_scan_result_pagination(self):
        """Test pagination capabilities."""
        # Test with page size
        # Note: API may return more than page_size if it has a minimum page size
        paginated_results = scan_result.list_scan_results(
            self.client,
            self.parent_namespace,
            list_params=ListParameters(page_size=5),
        )
        assert isinstance(paginated_results, list)
        assert len(paginated_results) > 0

    def test_scan_result_error_handling(self):
        """Test error handling for invalid UUID."""
        # Test with invalid UUID
        invalid_scan_result = scan_result.get_scan_result(
            self.client, self.parent_namespace, "invalid-uuid"
        )
        assert invalid_scan_result is None

    def test_scan_result_status_distribution(self):
        """Test and analyze scan result status distribution."""
        scan_results_list = scan_result.list_scan_results(
            self.client, self.parent_namespace
        )

        status_counts = {}
        type_counts = {}
        for sr in scan_results_list:
            if sr.spec:
                status = str(sr.spec.status) if sr.spec.status else "Unknown"
                status_counts[status] = status_counts.get(status, 0) + 1

                scan_type = str(sr.spec.type) if sr.spec.type else "Unknown"
                type_counts[scan_type] = type_counts.get(scan_type, 0) + 1

        print("\n=== Scan Result Status Distribution ===")
        print("Status distribution:")
        for status, count in status_counts.items():
            print(f"  {status}: {count}")

        print("Type distribution:")
        for scan_type, count in type_counts.items():
            print(f"  {scan_type}: {count}")

        assert len(scan_results_list) > 0


