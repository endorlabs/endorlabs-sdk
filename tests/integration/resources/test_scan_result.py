"""Test cases for ScanResult resource operations.

Tests GET and LIST operations for ScanResult resources. ScanResults are typically
auto-generated by endorctl scans and are read-only resources.

Greenfield alias unit tests live in
tests/unit/resources/test_greenfield_aliases.py.
"""

from datetime import UTC, datetime, timedelta

import pytest

import endorlabs
from endorlabs.core.types import ListParameters
from tests.conftest import (
    TEST_MAX_PAGES,
    TEST_PAGE_SIZE,
)


@pytest.mark.integration
class TestScanResult:
    """Test cases for ScanResult resource operations."""

    @pytest.fixture(autouse=True)
    def setup_fast(self, api_client, namespace, root_namespace) -> None:
        """Fast setup: client and namespace from conftest."""
        self.client = api_client
        self.namespace = namespace
        self.root_namespace = root_namespace
        self.endor_client = endorlabs.Client(tenant=namespace, api_client=api_client)
        self.endor_root_client = endorlabs.Client(
            tenant=root_namespace, api_client=api_client
        )

    @pytest.fixture
    def sample_scan_result(self):
        """Fetch minimal sample data (1 item) for UUID operations.

        Function-scoped but only fetches when explicitly requested by tests.
        Uses namespace client so resources in instance are captured.
        """
        results = self.endor_root_client.ScanResult.list(
            list_params=ListParameters(
                page_size=TEST_PAGE_SIZE,
            ),
            max_pages=TEST_MAX_PAGES,
        )
        if not results:
            pytest.skip("No resources in scope (empty; may be filter/auth/scope)")
        return results[0]  # Return single item, not list

    def test_scan_result_list(self) -> None:
        """LIST in namespace."""
        result = self.endor_client.ScanResult.list(
            max_pages=TEST_MAX_PAGES,
        )
        assert isinstance(result, list)

    def test_scan_result_list_with_parent_project(self) -> None:
        """LIST scan results with parent=project (list with parent resource)."""
        projects = self.endor_client.Project.list(
            max_pages=TEST_MAX_PAGES,
        )
        if not projects:
            pytest.skip("No projects in scope (empty; may be filter/auth/scope)")
        project = projects[0]
        result = self.endor_client.ScanResult.list(
            parent=project,
            max_pages=TEST_MAX_PAGES,
        )
        assert isinstance(result, list)

    def test_scan_result_get(self) -> None:
        """GET first item from LIST in namespace."""
        items = self.endor_client.ScanResult.list(
            max_pages=TEST_MAX_PAGES,
        )
        if not items:
            pytest.skip("No resources in scope (empty; may be filter/auth/scope)")
        item = items[0]
        got = self.endor_client.ScanResult.get(item)
        assert got is not None
        assert got.uuid == item.uuid

    def test_scan_result_filter_by_project(self, sample_scan_result) -> None:
        """Test filtering scan results by project UUID in the resource's namespace."""
        print("\n=== TESTING FILTER SCAN RESULTS BY PROJECT ===")

        first_scan = sample_scan_result
        if not first_scan.meta or not first_scan.meta.parent_uuid:
            pytest.skip("Scan result has no parent_uuid (project)")

        project_uuid = first_scan.meta.parent_uuid
        # Scope filter to same tenant/namespace as resource (tenant coupled to resource)
        list_namespace = (
            first_scan.tenant_meta.namespace
            if first_scan.tenant_meta
            and getattr(first_scan.tenant_meta, "namespace", None)
            else self.root_namespace
        )
        list_params = ListParameters(
            filter=f'meta.parent_uuid=="{project_uuid}"',
            page_size=TEST_PAGE_SIZE,
        )

        list_client = endorlabs.Client(tenant=list_namespace, api_client=self.client)
        filtered_results = list_client.ScanResult.list(
            list_params=list_params,
            max_pages=TEST_MAX_PAGES,
        )

        assert isinstance(filtered_results, list), (
            "Should return a list of filtered scan results"
        )
        if len(filtered_results) == 0:
            pytest.skip("No resources in scope (empty; may be filter/auth/scope)")

        # Verify all results belong to the project
        for result in filtered_results:
            assert result.meta.parent_uuid == project_uuid, (
                "All filtered results should belong to the project"
            )

        print(f"Found {len(filtered_results)} scan results for project {project_uuid}")

    def test_scan_result_list_parent_with_date_window(self, sample_scan_result) -> None:
        """LIST with parent=Project and from_date/to_date (pull_scan_results path)."""
        first_scan = sample_scan_result
        if not first_scan.meta or not first_scan.meta.parent_uuid:
            pytest.skip("Scan result has no parent_uuid (project)")

        project_uuid = first_scan.meta.parent_uuid
        list_namespace = (
            first_scan.tenant_meta.namespace
            if first_scan.tenant_meta
            and getattr(first_scan.tenant_meta, "namespace", None)
            else self.root_namespace
        )
        list_client = endorlabs.Client(tenant=list_namespace, api_client=self.client)
        project = list_client.Project.get(project_uuid, namespace=list_namespace)
        assert project is not None

        to_dt = datetime.now(UTC)
        from_dt = to_dt - timedelta(days=365)
        to_d = to_dt.isoformat().replace("+00:00", "Z")
        from_d = from_dt.isoformat().replace("+00:00", "Z")

        list_params = ListParameters(
            from_date=from_d,
            to_date=to_d,
            sort_by="meta.create_time",
            desc=True,
            page_size=TEST_PAGE_SIZE,
        )
        results = list_client.ScanResult.list(
            parent=project,
            list_params=list_params,
            max_pages=TEST_MAX_PAGES,
        )
        assert isinstance(results, list)
        if not results:
            pytest.skip(
                "No scan results in date window (empty; may be filter/auth/scope)"
            )
        for r in results:
            assert r.meta is not None
            assert r.meta.parent_uuid == project_uuid

    def test_scan_result_error_handling(self) -> None:
        """Test error handling for invalid UUID."""
        # Test with invalid UUID format - should raise ValidationError
        # (server returns HTTP 400 with gRPC code 3 INVALID_ARGUMENT)
        from endorlabs.core.exceptions import ValidationError

        with pytest.raises(ValidationError) as exc_info:
            self.endor_root_client.ScanResult.get("invalid-uuid")
        assert exc_info.value.resource_uuid == "invalid-uuid"
        assert exc_info.value.operation == "get"
        assert exc_info.value.status_code == 400
